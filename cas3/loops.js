// ciklusi / jamki (loops)

let a = 1
let b = 3


while (a <= b) {
  // iteracija
  const ime = "Test"
  console.log(ime)
  a++
}

do {
  const ime = "Test"
  console.log(ime)
  a++
} while (a <= b)


let brojac = 1

while (brojac <= 100) {
  console.log(brojac)
  brojac++
}


for (let brojac1; brojac1 <= 100; brojac1++) {
  console.log(brojac)
}

console.log("Done!")


